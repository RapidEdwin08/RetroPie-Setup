function build_lr-tyrquake() {
    make clean
    make
    md_ret_require="$md_build/tyrquake_libretro.so"
}

function install_lr-tyrquake() {
    md_ret_files=(
        'LICENSE.txt'
        'readme-id.txt'
        'readme.txt'
        'tyrquake_libretro.so'
    )
}

function game_data_lr-tyrquake() {
    if [[ ! -f "$romdir/ports/quake/id1/pak0.pak" ]]; then
        getDepends lhasa
        mkUserDir "$romdir/ports"
        mkUserDir "$romdir/ports/quake"
        local temp="$(mktemp -d)"
        # download / unpack / install quake shareware files
        downloadAndExtract "$__archive_url/quake106.zip" "$temp"
        pushd "$temp"
        lhasa ef resource.1
        cp -rf id1 "$romdir/ports/quake/"
        popd
        rm -rf "$temp"
        chown -R "$__user":"$__group" "$romdir/ports/quake"
        chmod 644 "$romdir/ports/quake/id1/"*
    fi
}

function _add_games_lr-tyrquake() {
    local cmd="$1"
    declare -A games=(
        ['id1']="Quake"
        ['hipnotic']="Quake I Mission Pack 1 (hipnotic)"
        ['rogue']="Quake I Mission Pack 2 (rogue)"
        ['dopa']="Quake I Episode 5 (dopa)"
    )
    local dir
    local pak
    for dir in "${!games[@]}"; do
        pak="$romdir/ports/quake/$dir/pak0.pak"
        if [[ -f "$pak" ]]; then
            addPort "$md_id" "quake" "${games[$dir]}" "$cmd" "$pak"
        fi
    done
}

function add_games_lr-tyrquake() {
    _add_games_lr-tyrquake "$md_inst/tyrquake_libretro.so"
}

function remove_lr-tyrquake() {
    local shortcut_name="Quake"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"

    shortcut_name="Quake I Episode 5 (dopa)"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"

    shortcut_name="Quake I Mission Pack 1 (hipnotic)"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"

    shortcut_name="Quake I Mission Pack 2 (rogue)"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"
}

function gui_lr-tyrquake() {
    choice=$(dialog --title "[$md_id] Configuration Options" --menu "      Get Additional Desktop Shortcuts + Icons\n\nGet Desktop Shortcuts for Additional Episodes + Add-Ons that may not have been present at Install\n\nSee [Package Help] for Details" 15 60 5 \
        "1" "Get Shortcuts + Icons" \
        "2" "Cancel" 2>&1 >/dev/tty)

    case $choice in
        1)
            add_games_lr-tyrquake
            shortcuts_icons_lr-tyrquake
            ;;
        2)
            echo "Canceled"
            ;;
        *)
            echo "Invalid Selection"
            ;;
    esac
}

function configure_lr-tyrquake() {
    setConfigRoot "ports"
    mkRomDir "ports/quake"

    [[ "$md_mode" == "install" ]] && game_data_lr-tyrquake

    add_games_lr-tyrquake

    defaultRAConfig "quake"

    #/opt/retropie/configs/ports/quake/retroarch.cfg aspect_ratio_index = "24"
    #[[ "$md_mode" == "remove" ]] && remove_lr-tyrquake
    if [[ ! -d "/opt/retropie/ports/tyrquake" ]] && [[ "$md_mode" == "remove" ]]; then remove_lr-tyrquake; fi
    [[ "$md_mode" == "remove" ]] && return
    [[ "$md_mode" == "install" ]] && shortcuts_icons_lr-tyrquake
}

