function depends_ionfury() {
    depends_eduke32
}

function sources_ionfury() {
    # patches are also shared with eduke32, so avoid duplication
    md_data="$(dirname $md_data)/eduke32" sources_eduke32
}

function build_ionfury() {
    build_eduke32
}

function install_ionfury() {
    install_eduke32
}

function remove_ionfury() {
    local shortcut_name
    shortcut_name="Ion Fury"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"

    shortcut_name="Ion Fury Aftershock"
    rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    rm -f "$romdir/ports/$shortcut_name.sh"
}

function configure_ionfury() {
    configure_eduke32
    if [[ ! -f "$md_conf_root/$md_id/aftershock_settings.cfg" ]]; then cp "$md_conf_root/$md_id/ionfury_settings.cfg" "$md_conf_root/$md_id/aftershock_settings.cfg"; fi

    # Ion Fury/Aftershock Standalone will use [config] withOUT [.cfg]
    # Leave Out ControllerButton11 = "" for AutoRun [Controls] "Use/Open" "Reload" "Walk" "Radar" "MedKit" "Last_Used_Weapon" "Quick_Swap_Electrifryer"
    cat > "$md_conf_root/ionfury/ionfury" << _EOF_
[Controls]
ControllerButton0 = "Jump"
ControllerButton1 = "Use/Open"
ControllerButton2 = "Toggle_Crouch"
ControllerButton3 = "Reload"
ControllerButton4 = "Map"
ControllerButton5 = "Third_Person_View"
ControllerButton7 = "Walk"
ControllerButton8 = "Center_View"
ControllerButton9 = "Previous_Weapon"
ControllerButton10 = "Next_Weapon"
ControllerButton12 = "MedKit"
ControllerButton13 = "Last_Used_Weapon"
ControllerButton14 = "Quick_Swap_Electrifryer"
ControllerAnalogDead1 = 4500
ControllerAnalogDead2 = 4500
ControllerAnalogDead3 = 4500
ControllerAnalogDead4 = 4500
_EOF_

    # aftershock.cfg [Controls] "Use/Open" "Reload" "Walk" "Swap_Pocket_Item" "MedKit" "Activate_Pocket_Item" "Quick_Swap_Baton/Vehicle_Turbo"
    cat > "$md_conf_root/ionfury/aftershock" << _EOF_
[Controls]
ControllerButton0 = "Jump"
ControllerButton1 = "Use/Open"
ControllerButton2 = "Toggle_Crouch"
ControllerButton3 = "Reload"
ControllerButton4 = "Map"
ControllerButton5 = "Third_Person_View"
ControllerButton7 = "Walk"
ControllerButton8 = "Center_View"
ControllerButton9 = "Previous_Weapon"
ControllerButton10 = "Next_Weapon"
ControllerButton12 = "MedKit"
ControllerButton13 = "Activate_Pocket_Item"
ControllerButton14 = "Quick_Swap_Baton/Vehicle_Turbo"
ControllerAnalogDead1 = 4500
ControllerAnalogDead2 = 4500
ControllerAnalogDead3 = 4500
ControllerAnalogDead4 = 4500
_EOF_
    chown -R $__user:$__user "$md_conf_root/$md_id"

    cat > "$romdir/ports/Ion Fury.sh" << _EOF_
#!/bin/bash
"/opt/retropie/supplementary/runcommand/runcommand.sh" 0 _PORT_ "$md_id" "-gamegrp \$HOME/RetroPie/roms/ports/ksbuild/ionfury/fury.grp -game_dir \$HOME/RetroPie/roms/ports/ksbuild/ionfury -cfg ionfury"
#"/opt/retropie/supplementary/runcommand/runcommand.sh" 0 _PORT_ "duke3d" "-gamegrp \$HOME/RetroPie/roms/ports/ksbuild/ionfury/fury.grp -game_dir \$HOME/RetroPie/roms/ports/ksbuild/ionfury -cfg ionfury"
_EOF_
    chown $__user:$__user "$romdir/ports/Ion Fury.sh"

    cat > "$md_inst/Ion Fury Aftershock.sh" << _EOF_
#!/bin/bash
"/opt/retropie/supplementary/runcommand/runcommand.sh" 0 _PORT_ "$md_id" "-gamegrp \$HOME/RetroPie/roms/ports/ksbuild/aftershock/fury.grp -game_dir \$HOME/RetroPie/roms/ports/ksbuild/aftershock -cfg aftershock"
#"/opt/retropie/supplementary/runcommand/runcommand.sh" 0 _PORT_ "duke3d" "-gamegrp \$HOME/RetroPie/roms/ports/ksbuild/aftershock/fury.grp -game_dir \$HOME/RetroPie/roms/ports/ksbuild/aftershock -cfg aftershock"
_EOF_
    if [[ -f "$romdir/ports/ksbuild/aftershock/fury.grp" ]]; then
        mv "$md_inst/Ion Fury Aftershock.sh" "$romdir/ports/Ion Fury Aftershock.sh"
        chown $__user:$__user "$romdir/ports/Ion Fury Aftershock.sh"
    fi

    [[ "$md_mode" == "install" ]] && shortcuts_icons_ionfury
    [[ "$md_mode" == "remove" ]] && remove_ionfury
}

