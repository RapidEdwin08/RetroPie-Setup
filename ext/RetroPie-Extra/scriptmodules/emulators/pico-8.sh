function _get_pico8_version() {
    # LIST Pico-8 Links
    local dl_link="www.lexaloffle.com/##/######"

    local dl_arch=amd64
    if [[ "$(_get_osbits_pico-8)" == '32' ]]; then dl_arch=i386; fi
    if [[ -f /proc/device-tree/model ]]; then dl_arch=raspi; fi

    # Get Latest Pico-8 Version # from changelog if connected
    if [[ "$has_net" -eq 0 ]]; then echo "${dl_link}/pico-8_0.2.7_${dl_arch}.zip"; return; fi
    curl -s "https://www.lexaloffle.com/dl/docs/pico-8_changelog.txt" -o "/dev/shm/pico8.log"
    # Extract latest version: search for lines that contain "vX.Y" with optional letter - Strip leading whitespace and keep unique top 1
    local dl_version=($(grep -E '^[[:space:]]*v[0-9]+\.[0-9]+[a-z]?' "/dev/shm/pico8.log" | sed -E 's/^[[:space:]]*//' | head -n 1))
    if [ ${#dl_version[@]} -eq 0 ]; then dl_version="0.2.7"; fi
    rm "/dev/shm/pico8.log"

    echo "${dl_link}/pico-8_${dl_version}_${dl_arch}.zip"
}

function depends_pico-8() {
    local depends=(libsdl2-dev)
    getDepends "${depends[@]}"
}

function _get_osbits_pico-8() {
    local _osbits=$(getconf LONG_BIT)
    echo $_osbits
}

function _get_arch_pico-8() {
    local _arch=amd64
    if [[ "$(_get_osbits_pico-8)" == '32' ]]; then _arch=i386; fi
    if [[ -f /proc/device-tree/model ]]; then _arch=raspi; fi
    echo $_arch
}

function _get_bin_pico-8() {
    local p8_bin=pico8
    if [[ ! "$(_get_arch_pico-8)" == 'raspi' ]] && [[ "$(_get_osbits_pico-8)" == '32' ]]; then p8_bin=pico8_32bit; fi
    if [[ "$(_get_arch_pico-8)" == 'raspi' ]] && [[ "$(_get_osbits_pico-8)" == '64' ]]; then p8_bin=pico8_64; fi
    echo $p8_bin
}

function _get_poombin_pico-8() {
    local poom_bin=poom_1.9
    ##if [[ ! "$(_get_arch_pico-8)" == 'raspi' ]] && [[ "$(_get_osbits_pico-8)" == '32' ]]; then poom_bin=poom_1.9_dyn; fi
    if [[ "$(_get_arch_pico-8)" == 'raspi' ]] && [[ "$(_get_osbits_pico-8)" == '64' ]]; then poom_bin=poom_1.9_64; fi
    echo $poom_bin
}

function _get_poomdir_pico-8() {
    local poom_dir=poom_1.9_linux
    if [[ "$(_get_arch_pico-8)" == 'raspi' ]]; then poom_dir=poom_1.9_raspi; fi
    echo $poom_dir
}

function check_bin_pico-8() {
    # This Check is called Only by the Configuration GUI
    if [[ "$(ls $home/pico-8 2>/dev/null | grep -v README.TXT)" == '' ]]; then
        md_ret_errors+=("\n\n        **** PICO-8 BINARY FILES NOT FOUND ****\n\n     **** YOU MUST PROVIDE THE PICO-8 BINARY ****")
        mkUserDir "$home/pico-8"
        if [[ ! -f "$home/pico-8/README.TXT" ]]; then echo "*PLACE PICO-8 BINARY FILES HERE*  Use the RetroPie-Setup pico-8 [Configuration / Options] to INSTALL/UPDATE your EXTRACTED Pico-8 Binary Files" >"$home/pico-8/README.TXT"; fi
        chown -R $__user:$__user "$home/pico-8"
        return
    fi
    install_bin_pico-8 gui
    #shortcuts_icons_pico-8
}

function install_bin_pico-8() {
    if [[ ! "$1"  == 'gui' ]]; then
        # Get Pico-8 Assets: P00M, MissingPico8 Graphic, media, and gamelist.xml
        downloadAndExtract "https://raw.githubusercontent.com/RapidEdwin08/RetroPie-Setup-Assets/main/emulators/pico-8-rp-assets.tar.gz" "$md_build"
        pushd "$md_build"

        # Use hidden folder [.media] due to [.p8.png] File Extension
        mkRomDir "pico8/.media"; mkRomDir "pico8/.media/image"; mkRomDir "pico8/.media/marquee"; mkRomDir "pico8/.media/video"
        mv '.media/image/P00M.png' "$romdir/pico8/.media/image"; mv '.media/marquee/P00M.png' "$romdir/pico8/.media/marquee"
        mv '.media/image/Splore.png' "$romdir/pico8/.media/image"; mv '.media/marquee/Splore.png' "$romdir/pico8/.media/marquee"
        if [[ ! -f "$romdir/pico8/gamelist.xml" ]]; then mv 'gamelist.xml' "$romdir/pico8"; else mv 'gamelist.xml' "$romdir/pico8/gamelist.xml.pico8"; fi
        if [[ ! -f "$romdir/pico8/+Start Splore.p8" ]]; then touch "$romdir/pico8/+Start Splore.p8"; fi
        if [[ ! -f "$romdir/pico8/P00M.p8" ]]; then touch "$romdir/pico8/P00M.p8"; fi
        chown -R $__user:$__user "$romdir/pico8"

        # MissingPico8 Graphic to be Displayed when attempting to Run ROM without pico8 Binary Installed
        mv 'MissingPico8.png' "$md_inst"
        popd

        # P00M
        mkUserDir "$md_inst/$(_get_poomdir_pico-8)"
        pushd "$md_build/$(_get_poomdir_pico-8)"; mv * "$md_inst/$(_get_poomdir_pico-8)"; popd
    fi

    mkUserDir "$home/pico-8"
    if [[ ! -f "$home/pico-8/README.TXT" ]]; then echo "*PLACE PICO-8 BINARY FILES HERE*  Use the RetroPie-Setup pico-8 [Configuration / Options] to INSTALL/UPDATE your EXTRACTED Pico-8 Binary Files $(_get_pico8_version)" >"$home/pico-8/README.TXT"; fi
    chown -R $__user:$__user "$home/pico-8"

    ##rm -f $home/.lexaloffle/pico-8/cdata/poom.p8d.txt > /dev/null 2>&1
    rm -Rf /dev/shm/pico-8 > /dev/null 2>&1
    cp -R $home/pico-8/ /dev/shm/ > /dev/null 2>&1
    mv /dev/shm/pico-8/* "$md_inst"
    chmod 755 "$md_inst/$(_get_bin_pico-8)" > /dev/null 2>&1
    chmod 755 "$md_inst/$(_get_poomdir_pico-8)/$(_get_poombin_pico-8)" > /dev/null 2>&1
    chown -R $__user:$__user "$md_inst/$(_get_poomdir_pico-8)"
    rm -Rf "$md_inst/README.TXT" > /dev/null 2>&1
    rm -Rf /dev/shm/pico-8 > /dev/null 2>&1
    rm -Rf "$md_build" > /dev/null 2>&1
}

function remove_pico-8() {
    local shortcut_name
    for shortcut_name in "Pico-8 (Splore)" "Poom"; do
        rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"
    done
    rm -f "$romdir/pico8/+Start Splore.p8"
    rm -f "$romdir/pico8/P00M.p8"
}

function gui_pico-8() {
    choice=$(dialog --title "[$md_id] Configuration Options" --menu "                 INSTALL [pico-8] Binary \n\n     **** YOU MUST PROVIDE THE PICO-8 BINARY ****\n\n$(_get_pico8_version)\n\nEXTRACT + PLACE your [Pico-8.zip/pico-8/*] Files into:\n $home/pico-8/" 20 60 5 \
        "1" "INSTALL [pico-8] Binary" \
        "2" "Cancel" 2>&1 >/dev/tty)

    case $choice in
        1)
            check_bin_pico-8
            #shortcuts_icons_pico-8
            ;;
        2)
            echo "Canceled"
            ;;
        *)
            echo "Invalid Selection"
            ;;
    esac
}

function configure_pico-8() {
    mkRomDir "pico8"

    addEmulator 1 "$md_id" "pico8" "$md_inst/pico8.sh %ROM%"
    addSystem "pico8"

    [[ "$md_mode" == "remove" ]] && rm -f $home/.lexaloffle/pico-8/cdata/poom.p8d.txt > /dev/null 2>&1
    [[ "$md_mode" == "remove" ]] && return

    mkUserDir "$home/.lexaloffle"
    mkUserDir "$md_conf_root/pico8/lexaloffle"
    moveConfigDir "$home/.lexaloffle" "$md_conf_root/pico8/lexaloffle"
    chown -R $__user:$__user "$md_conf_root/pico8"

    # disable retroarch built-in imageviewer so we can run .p8.png files
    defaultRAConfig "pico8" "builtin_imageviewer_enable" "false"

    cat >"$md_inst/pico8.sh" << _EOF_
#!/bin/bash

MissingPICO8() {
echo Pico-8 is NOT INSTALLED! INSTALL Pico-8 1st! >> /dev/shm/runcommand.log
[[ -f /opt/retropie/emulators/pico-8/MissingPico8.png ]] && sudo fbi -T 1 -a -noverbose /opt/retropie/emulators/pico-8/MissingPico8.png > /dev/null 2>&1; sleep 9; sudo kill \$(pgrep fbi) > /dev/null 2>&1; clear & clear
exit 0
}

ROMDIR="$home/RetroPie/roms/pico8"

ROM="\$1"
FILENAME="\$(basename -- "\$ROM")"

pushd /opt/retropie/emulators/pico-8
case \$FILENAME in
    "P00M.p8") # [CC BY-NC 4.0] https://creativecommons.org/licenses/by-nc/4.0/
        echo "[\$FILENAME]" >> /dev/shm/runcommand.log 2>&1
        [[ ! -f "$md_inst/$(_get_poomdir_pico-8)/$(_get_poombin_pico-8)" ]] && MissingPICO8
        popd; pushd /opt/retropie/emulators/pico-8/$(_get_poomdir_pico-8)
        ./$(_get_poombin_pico-8)
    ;;
    *"Splore.p8")
        echo "[\$FILENAME]" >> /dev/shm/runcommand.log 2>&1
        [[ ! -f "$md_inst/$(_get_bin_pico-8)" ]] && MissingPICO8
        ./$(_get_bin_pico-8) -splore -root_path "\$ROMDIR"
    ;;
    *)
        [[ ! -f "$md_inst/$(_get_bin_pico-8)" ]] && MissingPICO8
        ./$(_get_bin_pico-8) -root_path "\$ROMDIR" -run "\$ROM"
    ;;
esac
popd

_EOF_
    chmod 755 "$md_inst/pico8.sh"

    [[ "$md_mode" == "install" ]] && shortcuts_icons_pico-8
}

