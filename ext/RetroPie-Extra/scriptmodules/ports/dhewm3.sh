    depends+=(xorg matchbox-window-manager)
    getDepends "${depends[@]}"
}

function sources_dhewm3() {
    gitPullOrClone
    [[ "$md_mode" == "install" ]] && git clone --branch cdoom --depth 1 https://github.com/dhewm/dhewm3-sdk.git "$md_build/dhewm3-sdk"
    #downloadAndExtract https://github.com/dhewm/dhewm3/releases/download/1.5.4/dhewm3-mods-1.5.4_Linux_amd64.tar.gz "$md_build/dhewm3-mods"
}

function build_dhewm3() {
    mkdir "$md_build/build"
    cd "$md_build/build"
    cmake "../neo"
    make clean
    make
    md_ret_require="$md_build/build"

    mkdir "$md_build/dhewm3-sdk/build"
    cd "$md_build/dhewm3-sdk/build"
    cmake ..
    make -j"$(nproc)"

    md_ret_require=(
        "$md_build/build"
        "$md_build/dhewm3-sdk/build/cdoom.so"
    )
}

function install_dhewm3() {
    md_ret_files=(
        "build/dhewm3"
        "build/d3xp.so"
        "build/base.so"
        "dhewm3-sdk/build/cdoom.so"
	"base"
    )
}

