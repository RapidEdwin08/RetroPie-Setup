function game_data_quakespasm() {
    if [[ ! -f "$romdir/ports/quake/id1/pak0.pak" ]]; then
        getDepends lhasa
        mkUserDir "$romdir/ports"
        mkUserDir "$romdir/ports/quake"
        local temp="$(mktemp -d)"
        # download / unpack / install quake shareware files
        downloadAndExtract "$__archive_url/quake106.zip" "$temp"
        pushd "$temp"
        lhasa ef resource.1
        cp -rf id1 "$romdir/ports/quake/"
        popd
        rm -rf "$temp"
        chown -R "$__user":"$__group" "$romdir/ports/quake"
        chmod 644 "$romdir/ports/quake/id1/"*
    fi

    # honey2022 wget https://www.slipseer.com/index.php?resources/honey.88/download -O honey-2022.zip
    if [[ "$(ls -1 "$romdir/ports/quake/honey" 2>/dev/null)" == '' ]]; then
        getDepends unzip
        mkUserDir "$romdir/ports/quake/honey"
        wget https://www.slipseer.com/index.php?resources/honey.88/download -O /dev/shm/honey-2022.zip
        unzip -qq -o /dev/shm/honey-2022.zip -d "$romdir/ports/quake/honey"
        chown -R $__user:$__user "$romdir/ports/quake/honey"
        rm -f /dev/shm/honey-2022.zip
    fi

    # honey2012 wget https://www.quaddicted.com/filebase/honey.zip
    if [[ "$(ls -1 "$romdir/ports/quake/honey" 2>/dev/null)" == '' ]]; then
        getDepends unzip
        mkUserDir "$romdir/ports/quake/honey"
        wget https://www.quaddicted.com/filebase/honey.zip -O /dev/shm/honey.zip
        unzip -qq -o /dev/shm/honey.zip -d "$romdir/ports/quake/honey"
        chown -R $__user:$__user "$romdir/ports/quake/honey"
        rm -f /dev/shm/honey.zip
    fi
}

function _add_games_quakespasm() {
    local cmd="$1"
    declare -A games=(
        ['id1']="Quake"
        ['hipnotic']="Quake I Mission Pack 1 (hipnotic)"
        ['rogue']="Quake I Mission Pack 2 (rogue)"
        ['dopa']="Quake I Episode 5 (dopa)"
    )
    local dir
    local pak
    for dir in "${!games[@]}"; do
        pak="$romdir/ports/quake/$dir/pak0.pak"
        if [[ -f "$pak" ]]; then
            addPort "$md_id" "quake" "${games[$dir]}" "$cmd" "$pak"
        fi
    done

    if [[ ! "$(ls -1 "$romdir/ports/quake/honey" 2>/dev/null)" == '' ]]; then
        addPort "$md_id" "quake" "Quake I AddOn Honey" "$binary ${params[*]}" "$romdir/ports/quake/honey/pak0.pak"
    fi
}

function add_games_quakespasm() {
    local params=("%QUAKEDIR%")
    local binary="$md_inst/quakespasm.sh"

    isPlatform "kms" && params+=("-width %XRES%" "-height %YRES%")

    _add_games_quakespasm "$binary ${params[*]}"
}

function remove_quakespasm() {
    local shortcut_name
    for shortcut_name in "Quake" "Quake I Episode 5 (dopa)" "Quake I Mission Pack 1 (hipnotic)" "Quake I Mission Pack 2 (rogue)" "Quake I AddOn Honey"; do
        rm -f "/usr/share/applications/$shortcut_name.desktop"; rm -f "$home/Desktop/$shortcut_name.desktop"; rm -f "$romdir/ports/$shortcut_name.sh"
    done
}

