        iniSet "osdscale" 2.500000
        isPlatform "gl" || isPlatform "mesa" && iniSet "r_vsync" 1
        ##iniSet "r_useindexedcolortextures" "0"
        ##iniSet "r_usenewshading" "3"
        ##iniSet "r_ambientlight" 1
        ##iniSet "r_shadows" "1"
        chown "$__user":"$__user" "$config"

        cat > "$md_conf_root/amcsquad/amcsquad.cfg" << _EOF_
[Controls]
ControllerButton0 = "Jump"
ControllerButton1 = "Open"
ControllerButton2 = "Toggle_Crouch"
ControllerButton3 = "MELEE_ATTACK"
