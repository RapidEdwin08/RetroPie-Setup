function _get_commit_amcduke32() {
    # Pull Latest Commit SHA - Allow RP Module Script to Check against Latest Source
    local branch_tag=master
    local branch_commit="$(git ls-remote https://github.com/dibollinger/amcduke32.git $branch_tag HEAD | grep $branch_tag | tail -1 | awk '{ print $1}' | cut -c -8)"

    #echo d6a40c36; # dibollinger-master # 20251022 [v5.22] AMC: Further simplify the savegame subdirectory function
    echo $branch_commit
}

function depends_amcduke32() {
    local depends=(
        flac libflac-dev libvorbis-dev libpng-dev libvpx-dev freepats
        libsdl2-dev libsdl2-mixer-dev unzip
    )
    depends+=(libsdl1.2-dev libsdl-mixer1.2-dev) # AMCDuke32
    isPlatform "x86" && depends+=(nasm)
    isPlatform "gl" || isPlatform "mesa" && depends+=(libgl1-mesa-dev libglu1-mesa-dev)
    isPlatform "x11" && depends+=(libgtk2.0-dev)
    getDepends "${depends[@]}"
}

function sources_amcduke32() {
    gitPullOrClone

    # Updated Controller config
    applyPatch "$md_data/controller-buttons-dibollinger-d6a40c36.diff"

    # https://voidpoint.io/terminx/eduke32/-/work_items/309
    ( isPlatform "rpi"* || isPlatform "arm" ) && applyPatch "$md_data/sbc-opengl-dibollinger-d6a40c36.diff"

    # Remove [MenuEntry_HideOnCondition] for the [Renderer:] Entry in Menu # [‾] console -> [setrendermode 0]
    ( isPlatform "gl" || isPlatform "mesa" ) && applyPatch "$md_data/remove-MenuEntry_HideOnCondition-dibollinger-d6a40c36.diff"

    # emileb-main_mobile-b564dd63 # Update indexed textures as GL_ALPHA instead of GL_RED to avoid promotion to GL_RGB inside GL4ES, saves 50% memory
    ( isPlatform "rpi"* || isPlatform "arm" ) && applyPatch "$md_data/replace-gl_red-dibollinger-d6a40c36.diff"
}

function build_amcduke32() {
    local params=(LTO=0 SDL_TARGET=2 STARTUP_WINDOW=0)
    params+=(APPBASENAME=amcsquad APPNAME=AMCDuke32 NETCODE=0 POLYMER=0) # AMCDuke32
    if ! ( isPlatform "gl" || isPlatform "mesa" ); then params+=(USE_OPENGL=0); fi
    #! isPlatform "x86" && params+=(NOASM=1) # AMCDuke32 Autosave 0N
    ! isPlatform "x11" && params+=(HAVE_GTK2=0)

    make veryclean
    CFLAGS+=" -DSDL_USEFOLDER" make -j"$(nproc)" "${params[@]}"

    md_ret_require="$md_build/amcsquad"
}

function install_amcduke32() {
    md_ret_files=(
