}

function configure_amcduke32() {
    mkRomDir "ports/ksbuild"
    mkRomDir "ports/ksbuild/amcsquad"
    chown -R $__user:$__user "$romdir/ports/ksbuild"

    moveConfigDir "$home/.config/amcsquad" "$md_conf_root/amcsquad"

    local launch_prefix
    ##isPlatform "kms" && launch_prefix="XINIT-WM:"
    addPort "$md_id" "amcsquad" "AMC Squad" "${launch_prefix}$md_inst/amcsquad.sh %ROM%" "-cfg $md_conf_root/amcsquad/amcsquad.cfg"

    if [[ "$md_mode" == "install" ]]; then
        local launsh_prefix
        isPlatform "kms" && launsh_prefix="VC4_DEBUG=always_sync " # SDL_VIDEODRIVER=x11
        # launcher script
        cat > "$md_inst/amcsquad.sh" << _EOF_
#!/bin/bash
pushd $romdir/ports/ksbuild/amcsquad
${launsh_prefix}$md_inst/amcsquad ¥$*
popd
_EOF_
        chmod 755 "$md_inst/amcsquad.sh"

        local config="$romdir/ports/ksbuild/amcsquad/autoexec.cfg"
        touch "$config"
        iniConfig " " '"' "$config"
        # autoexec settings - [snd_numvoices 128] only Default Setting included with [AMCSquad]
